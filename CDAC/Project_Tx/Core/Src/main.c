void Ultrasonic_distance(void)
{
	//Send Trigger Signal to ultrasonic trigger pin
	HAL_GPIO_WritePin(ULTRASONIC_TRIGGER_PORT, ULTRASONIC_TRIGGER_PIN, GPIO_PIN_SET);		//pull the trig pin high
	HAL_Delay(1);
	HAL_GPIO_WritePin(ULTRASONIC_TRIGGER_PORT, ULTRASONIC_TRIGGER_PIN, GPIO_PIN_RESET);		//pull the trig pin low
	//Enable Timer counter
	__HAL_TIM_ENABLE_IT(ULTRASONIC_ECHO_PIN_IC, TIM_IT_CC1);
}


void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	//Capture Rising Edge
	if(isRisingCaptured == 0)		//if the first value is not captured
	{
		InputCaptureValue_1 = HAL_TIM_ReadCapturedValue(&htim4, TIM_CHANNEL_1);		//Read the First value
		isRisingCaptured = 1;			//Set the isRisingCaptured as true
		__HAL_TIM_SET_CAPTUREPOLARITY(&htim4, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);		//Now change the polarity to the falling edge
	}
	//Capture Falling Edge
	else if(isRisingCaptured == 1)		//if the first is already captured
	{
		InputCaptureValue_2 = HAL_TIM_ReadCapturedValue(&htim4, TIM_CHANNEL_1);		//read second value
		__HAL_TIM_SET_COUNTER(&htim4, 0);		// reset the counter

		if(InputCaptureValue_2 > InputCaptureValue_1)
		{
			InputCaptureDifference = InputCaptureValue_2 - InputCaptureValue_1;
		}
		else if(InputCaptureValue_1 > InputCaptureValue_2)
		{
			InputCaptureDifference = (0xFFFF - InputCaptureValue_1) + InputCaptureValue_2;
		}

		distance = InputCaptureDifference * 0.0173;

		isReadingFinished = 1;

		isRisingCaptured = 0;		//set it back to false

		__HAL_TIM_SET_CAPTUREPOLARITY(&htim4, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);		//set the polarity to rising edge
		__HAL_TIM_DISABLE_IT(&htim4, TIM_IT_CC1);		//Disable timer counter

	}

}

